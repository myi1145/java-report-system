<script setup lang="ts">
import { ElRow, ElCol, ElCard, ElSkeleton, ElButton } from 'element-plus'
import { CountTo } from '@/components/CountTo'
import { useDesign } from '@/hooks/web/useDesign'
import { useI18n } from '@/hooks/web/useI18n'
import { ref, reactive } from 'vue'
import { getCountApi } from '@/api/dashboard/analysis'
import type { AnalysisTotalTypes } from '@/api/dashboard/analysis/types'
import { useUserStore } from '@/store/modules/user'
import { Dialog } from '@/components/Dialog'
import Transplant from '@/views/Dashboard/components/Transplant.vue'
import InHospital from '@/views/Dashboard/components/InHospital.vue'

const userStore = useUserStore()

const dialogVisible2 = ref(false)
const dialogVisible = ref(false)

const { t } = useI18n()

const { getPrefixCls } = useDesign()

const prefixCls = getPrefixCls('panel')

const loading = ref(true)

let totalState = reactive<AnalysisTotalTypes>({
  visitsSum: 0,
  transplantSum: 0,
  moneys: 0,
  inHospSum: 0
})

const getCount = async () => {
  const res = await getCountApi(userStore.getUserInfo?.username || '')
    .catch(() => {})
    .finally(() => {
      loading.value = false
    })
  totalState = Object.assign(totalState, res?.data || {})
}

getCount()
</script>

